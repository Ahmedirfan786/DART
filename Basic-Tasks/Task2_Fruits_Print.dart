void main(){
  var fruits=["Apple","Banana","Coconut","Dates","Elderberry","Fig","Guava","Honeydew","IceCreamBean","Jackfruit","Kiwi","Lemon","Mango","Nectarine","Orange","PineApple","Quince","Raspberry","Strawberry","Tangerine","Uglifruit","VineyardPeach","WaterMelon","Xigua","Yuzu","Zucchini"];
  
  for(var i=0; i< fruits.length; i++){
    print(fruits[i]);
  }
}


// The main() function is the entry point of the program.

// A list variable named fruits is defined, containing a list of fruit names. Each fruit name is a string enclosed in double quotes.

// The for loop iterates over each element of the fruits array. It initializes the loop variable i to 0, and the loop continues as long as i is less than the length of the fruits array (fruits.length). After each iteration, i is incremented by 1.

// Inside the loop, fruits[i] is used to access the element at index i of the fruits array, and print() is used to print each fruit name to the console.

// So, when the program is executed, it will print each fruit name in the fruits array, one by one, to the console.